package MenuDriven;

public class Arithmetic {
    public static double addition(double no1,double no2){
      return no1+no2;
    }
    public static double subtraction(double no1,double no2){
        return no1-no2;
    }
    public static double multiplication(double no1,double no2){
        return no1*no2;
    }
    public static double division(double no1,double no2){
        return no1/no2;
    }
}
